public class Exercicio1_4{
	public static void main(String[] args){
				float a = 45, b = 20, c = 3;
				float M, D, Su, So;
				M = a * b * c;
				D = a / b / c;
				Su = a - b - c;
				So = a + b + c;
				System.out.println("O valor da Multiplicação foi: " + M);
				System.out.println("O valor da Divisão foi: " + D);
				System.out.println("O valor da Subtração foi: " + Su);
				System.out.println("O valor da Soma foi: " + So);
	}
	
}
